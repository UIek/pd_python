class Wzory():
    def ciezar(m, g):
        print(f'Fg = {m*g}')
    def gestosc(m, v):
        print(f'p = {m/v}')
    def sila_wyporu(pc, g, Vc):
        print(f'Fw = {pc*g*Vc}')
    def predkosc(s, t):
        print(f'v = {s/t}')
    def droga(v, t):
        print(f's = {v*t}')
    def czas(v, s):
        print(f't = {s/v}')
    def przyspieszenie(v, t):
        print(f'a = {v/t}')
    def ped_ciala( m, v):
        print(f'p = {m*v}')
    def praca( F, s):
        print(f'W = {F*s}')
    def moc(W, t):
        print(f'P = {W/t}')
    def energia_potencjalna( m, g, h):
        print(f'Ep = {m*g*h}')
    def energia_kinetyczna(m, v):
        print(f'Ek = {v**2*m*1/2}')

    


